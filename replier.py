def format_task(options_formatted):
	"""
	Command: /task
	"""
	text = md.text(
		md.text(
			md_quote('–£–∫–∞–∂–∏ –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–æ–≤, –≤ –∫–æ—Ç–æ—Ä—ã—Ö'),
			md.bold('–≤–µ—Ä–Ω–æ'),
			md_quote('–≤—ã–¥–µ–ª–µ–Ω–∞ –±—É–∫–≤–∞, –æ–±–æ–∑–Ω–∞—á–∞—é—â–∞—è —É–¥–∞—Ä–Ω—ã–π –≥–ª–∞—Å–Ω—ã–π –∑–≤—É–∫.'),
			md_quote('–ó–∞–ø–∏—à–∏ –Ω–æ–º–µ—Ä–∞ –æ—Ç–≤–µ—Ç–æ–≤.'),
			sep=' '
		),
		md_quote(options_formatted),
		sep='\n'
	)
	return text


